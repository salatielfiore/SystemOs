    private void obterValor() {
        try {
            OsDal dal = new OsDal();
            txt_os.setText(id_os);
            txt_data.setText(data_os);

            if (tipo.equals(rb_orcamento.getText())) {
                rb_orcamento.setSelected(true);
            } else {
                rb_ordem_servico.setSelected(true);
            }

            cb_situacao.setSelectedItem(situacao);
            txt_area_descricao.setText(descricao);
            txt_equipamento.setText(equipamento);
            txt_defeito.setText(defeito);
            txt_servico.setText(servico);
            cb_tecnico.setSelectedItem(tecnico);
            txt_valor_total.setText(valor);
            txt_cod_cliente.setText(idcliente);

            //dados do cliente
            dal.valorCliente(idcliente);

            txt_nome.setText(nome);
            txt_telefone.setText(telefone);
        } catch (Exception e) {
        }

    }

    public static void valor(Os os) {
        id_os = String.valueOf(os.getCodigo());
        data_os = os.getData();
        tipo = os.getTipo();
        situacao = os.getSituacao();
        descricao = os.getDescricao();
        equipamento = os.getEquipamento();
        defeito = os.getDefeito();
        servico = os.getServico();
        tecnico = os.getTecnico();
        valor = String.valueOf(os.getValor());
        idcliente = String.valueOf(os.getCod_cliente());

    }

    public static void valor(Cliente cliente) {
        nome = cliente.getNome();
        telefone = cliente.getTelefone();
    }

    private void getValTecUsuario() {

        String sql = "select * from tbusuario where cargo = 'Tecnico em Informatica' ";
        UsuarioDal usuarioDal = new UsuarioDal();
        for (Usuario u : usuarioDal.listarTodos(sql)) {
            cb_tecnico.addItem(u.getNome());
        }
    }

    public void getNomeUser(String nomeUser) {
        this.nomeUser = nomeUser;
    }

