    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
        // TODO add your handling code here:
        String tipos = "";

        if (txt_os.getText().equals("")) {
            //se o campo codigo estiver vazio insere um novo cliente

            Os os = new Os();
            OsDal osDal = new OsDal();

            if (txt_cod_cliente.getText().equals("")
                    || txt_area_descricao.getText().equals("")
                    || txt_equipamento.getText().equals("")) {

                JOptionPane.showMessageDialog(null, "Exite campos vazio, preencha todos os campos");

            } else {

                //verifica se o botão (radioButton) rb_orcamento esta selecionado
                //e atribui o valor a variavel tipos
                if (rb_orcamento.isSelected()) {
                    tipos = "Orçamento";
                } else {
                    tipos = "Ordem de serviço";
                }
                os.setTipo(tipos);

                os.setSituacao(cb_situacao.getSelectedItem().toString());
                os.setEquipamento(txt_equipamento.getText());
                os.setServico(txt_servico.getText());

                os.setTecnico((String) cb_tecnico.getSelectedItem());
                os.setDescricao(txt_area_descricao.getText());
                os.setDefeito(txt_defeito.getText());
                os.setValor(txt_valor_total.getText());
                os.setCod_cliente(Integer.parseInt(txt_cod_cliente.getText()));
                osDal.setCodUser(nomeUser);
                osDal.inserir(os);

                String sql = "SELECT * FROM tbos";
                tb_os.setModel(new OsTableModel(new OsDal().listarTodos(sql)));
                limparCampo();
                obterValor();
                btn_salvardesabilitado();

                desabilitarCampo();
            }
        } else {
            //altera o usuario
            if (txt_cod_cliente.getText().equals("")
                    || txt_area_descricao.getText().equals("")
                    || txt_equipamento.getText().equals("")) {

                JOptionPane.showMessageDialog(null, "Exite campos vazio, preencha todos os campos");

            } else {
                Os os = new Os();
                OsDal osDal = new OsDal();

                if (rb_orcamento.isSelected()) {
                    tipos = "Orçamento";
                } else {
                    tipos = "Ordem de serviço";
                }
                os.setTipo(tipos);

                os.setSituacao(cb_situacao.getSelectedItem().toString());

                os.setEquipamento(txt_equipamento.getText());
                os.setServico(txt_servico.getText());
                os.setTecnico((String) cb_tecnico.getSelectedItem());
                os.setDescricao(txt_area_descricao.getText());
                os.setDefeito(txt_defeito.getText());
                os.setValor(txt_valor_total.getText());
                os.setCod_cliente(Integer.parseInt(txt_cod_cliente.getText()));
                os.setCodigo(Integer.parseInt(txt_os.getText()));

                osDal.alterar(os);
                String sql = "SELECT * FROM tbos";
                tb_os.setModel(new OsTableModel(new OsDal().listarTodos(sql)));

                btn_salvardesabilitado();
                desabilitarCampo();

            }
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

